package com.spring.model.menu;

public class MainMenu {
	
	int first_order;
	
	int second_order;
	
	String name;
	
	String menu_link;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getFirst_order() {
		return first_order;
	}

	public void setFirst_order(int first_order) {
		this.first_order = first_order;
	}

	public int getSecond_order() {
		return second_order;
	}

	public void setSecond_order(int second_order) {
		this.second_order = second_order;
	}

	public String getMenu_link() {
		return menu_link;
	}

	public void setMenu_link(String menu_link) {
		this.menu_link = menu_link;
	}

}
