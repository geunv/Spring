package com.spring.model.setting;

public class UserInfoModel {
	String plant_cd;
	String user_pwd;
	String user_grp;
	String user_nm;
	String web_user_permit;
	
	public String getPlant_cd() {
		return plant_cd;
	}
	public void setPlant_cd(String plant_cd) {
		this.plant_cd = plant_cd;
	}
	public String getUser_pwd() {
		return user_pwd;
	}
	public void setUser_pwd(String user_pwd) {
		this.user_pwd = user_pwd;
	}
	public String getUser_grp() {
		return user_grp;
	}
	public void setUser_grp(String user_grp) {
		this.user_grp = user_grp;
	}
	public String getUser_nm() {
		return user_nm;
	}
	public void setUser_nm(String user_nm) {
		this.user_nm = user_nm;
	}
	public String getWeb_user_permit() {
		return web_user_permit;
	}
	public void setWeb_user_permit(String web_user_permit) {
		this.web_user_permit = web_user_permit;
	}
	
	
}
