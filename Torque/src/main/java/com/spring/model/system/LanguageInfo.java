package com.spring.model.system;

public class LanguageInfo {
	String plant_cd;
	String sys_area;
	String data_type;
	String msg_type;
	String msg_text_ko_kr;
	String msg_text_en_us;
	String msg_text_lo_ln;
	
	public String getPlant_cd() {
		return plant_cd;
	}
	public void setPlant_cd(String plant_cd) {
		this.plant_cd = plant_cd;
	}
	public String getSys_area() {
		return sys_area;
	}
	public void setSys_area(String sys_area) {
		this.sys_area = sys_area;
	}
	public String getData_type() {
		return data_type;
	}
	public void setData_type(String data_type) {
		this.data_type = data_type;
	}
	public String getMsg_type() {
		return msg_type;
	}
	public void setMsg_type(String msg_type) {
		this.msg_type = msg_type;
	}
	public String getMsg_text_ko_kr() {
		return msg_text_ko_kr;
	}
	public void setMsg_text_ko_kr(String msg_text_ko_kr) {
		this.msg_text_ko_kr = msg_text_ko_kr;
	}
	public String getMsg_text_en_us() {
		return msg_text_en_us;
	}
	public void setMsg_text_en_us(String msg_text_en_us) {
		this.msg_text_en_us = msg_text_en_us;
	}
	public String getMsg_text_lo_ln() {
		return msg_text_lo_ln;
	}
	public void setMsg_text_lo_ln(String msg_text_lo_ln) {
		this.msg_text_lo_ln = msg_text_lo_ln;
	}
	
}
